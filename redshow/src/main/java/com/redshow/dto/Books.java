package com.redshow.dto;

public class Books {
	
	private int id;
	
	private String booksNames;
	
	private Double booksPrice;
	
	private String booksRemake;
	
	private String imgTypes;
	
	private String booksUrl;
	
	private int booksTypeId;
	
	private int state;
	
	private int userId;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getBooksNames() {
		return booksNames;
	}

	public void setBooksNames(String booksNames) {
		this.booksNames = booksNames;
	}

	public Double getBooksPrice() {
		return booksPrice;
	}

	public void setBooksPrice(Double booksPrice) {
		this.booksPrice = booksPrice;
	}

	public String getBooksRemake() {
		return booksRemake;
	}

	public void setBooksRemake(String booksRemake) {
		this.booksRemake = booksRemake;
	}

	public String getImgTypes() {
		return imgTypes;
	}

	public void setImgTypes(String imgTypes) {
		this.imgTypes = imgTypes;
	}

	public String getBooksUrl() {
		return booksUrl;
	}

	public void setBooksUrl(String booksUrl) {
		this.booksUrl = booksUrl;
	}

	public int getBooksTypeId() {
		return booksTypeId;
	}

	public void setBooksTypeId(int booksTypeId) {
		this.booksTypeId = booksTypeId;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}
	
	
}
